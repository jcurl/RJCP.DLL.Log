        };

        public static int Convert(ReadOnlySpan<byte> bytes, char[] chars)
        {
            int cu = bytes.Length < chars.Length ? bytes.Length : chars.Length;
            for (int i = 0; i < cu; i++) {
                chars[i] = Iso8859_15Map[bytes[i]];
            }
            return cu;
        }
    }
}
